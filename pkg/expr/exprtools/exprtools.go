// Package exprtools provides additional functionality gadgets on top of expr
// package.
//
// Part of the design of expr package is to be as simple as possible. This
// design might be beneficial for further analysis of the program. On the other
// hand this design choice makes it non-trivial to describe many common
// operations of nowadays CPUs. For this reason this package provides more some
// additional gadgets implemented using expr package statements and implementing
// more high-level operations.
package exprtools
